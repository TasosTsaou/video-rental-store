package com.tasos.sampleapi.client.config;

public class UrlRootInfo {

    private String host;

    private String port;

    public UrlRootInfo() {

    }

    public String getHost() {
        return host;
    }

    public void setHost(String host) {
        this.host = host;
    }

    public String getPort() {
        return port;
    }

    public void setPort(String port) {
        this.port = port;
    }

}
